x <- 11
y <- 4

# addition
print(x+y) #15

# subtraction
print(x-y) #7

# multiplication
print(x*y) #44

# division
print(x/y) #2.75

# exponentiation
print(x^y) #or x**y #14641

# modulus
print(x%%y) #3

# integer division
print(x%/%y) #2

# Math functions
print(min(x, y))
print(max(x, y))

print(sqrt(y))

# Booleans
print(x>y)
print(x<y)


